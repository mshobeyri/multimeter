#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

function escapeForTemplateLiteral(s) {
  return s
    .replace(/`/g, '\\`')
    .replace(/\$\{/g, '\\${');
}

const repoRoot = path.resolve(__dirname, '..', '..');
const srcHtml = path.join(repoRoot, 'res', 'doc-template.html');
const outDir = path.join(repoRoot, 'core', 'src');
const outFile = path.join(outDir, 'docTemplate.ts');

try {
  const raw = fs.readFileSync(srcHtml, 'utf8');
  const body = escapeForTemplateLiteral(raw);
  const ts = `// Auto-generated by core/scripts/gen-doc-template.cjs
export const DOC_TEMPLATE_HTML = \`
${body}
\`;
`;
  fs.writeFileSync(outFile, ts, 'utf8');
  console.log(`[gen-doc-template] Wrote ${path.relative(repoRoot, outFile)}`);
} catch (err) {
  // Generate an empty export to allow fallback path in consumer
  const ts = `// Auto-generated fallback (source template missing)
export const DOC_TEMPLATE_HTML = '';
`;
  try {
    fs.writeFileSync(outFile, ts, 'utf8');
    console.warn(`[gen-doc-template] WARNING: Missing template, wrote empty module: ${path.relative(repoRoot, outFile)}`);
  } catch (e) {
    console.error('[gen-doc-template] ERROR writing fallback module:', e);
    process.exit(1);
  }
}
